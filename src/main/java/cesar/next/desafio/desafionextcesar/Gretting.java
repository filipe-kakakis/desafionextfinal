package cesar.next.desafio.desafionextcesar;

public class Gretting {
    private long id;
    private String content;

    public Gretting(long id, String content) {
        this.id = id;
        this.content = content;
    }

    public long getId() {
        return id;
    }

    public String getContent() {
        return content;
    }
}
